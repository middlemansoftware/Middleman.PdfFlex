namespace Middleman.PdfFlex.Styling;

/// <summary>
/// Specifies the weight (boldness) of a font, following the CSS/OpenType weight scale.
/// </summary>
public enum FontWeight
{
    /// <summary>Thin (Hairline). Weight 100.</summary>
    Thin = 100,

    /// <summary>Extra light (Ultra light). Weight 200.</summary>
    ExtraLight = 200,

    /// <summary>Light. Weight 300.</summary>
    Light = 300,

    /// <summary>Normal (Regular). Weight 400.</summary>
    Normal = 400,

    /// <summary>Medium. Weight 500.</summary>
    Medium = 500,

    /// <summary>Semi bold (Demi bold). Weight 600.</summary>
    SemiBold = 600,

    /// <summary>Bold. Weight 700.</summary>
    Bold = 700,

    /// <summary>Extra bold (Ultra bold). Weight 800.</summary>
    ExtraBold = 800,

    /// <summary>Black (Heavy). Weight 900.</summary>
    Black = 900
}
