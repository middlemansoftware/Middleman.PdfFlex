namespace Middleman.PdfFlex.Styling;

/// <summary>
/// Specifies horizontal text alignment within a text block.
/// </summary>
public enum TextAlign
{
    /// <summary>Align text to the left edge.</summary>
    Left,

    /// <summary>Center text horizontally.</summary>
    Center,

    /// <summary>Align text to the right edge.</summary>
    Right,

    /// <summary>Justify text by adjusting word spacing to fill the line width.</summary>
    Justify
}
