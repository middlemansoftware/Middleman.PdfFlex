namespace Middleman.PdfFlex.Styling;

/// <summary>
/// Specifies the style (posture) of a font.
/// </summary>
public enum FontStyle
{
    /// <summary>Normal upright text.</summary>
    Normal,

    /// <summary>Italic text, using a dedicated italic typeface when available.</summary>
    Italic,

    /// <summary>Oblique text, a slanted version of the normal typeface.</summary>
    Oblique
}
